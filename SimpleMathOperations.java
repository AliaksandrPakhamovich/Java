public class SimpleMathOperations {
    public static void main(String[] args) {
        int q = 10;
        int w =  3;

        System.out.println(q + w);
        System.out.println(q - w);
        System.out.println(q * w);
        System.out.println(q / w);
        System.out.println(q % w);


    }
}
